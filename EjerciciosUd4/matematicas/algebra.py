# matematicas/algebra.py

def suma(a, b):
    """
    Realiza la suma de dos números.

    Args:
        a (float): El primer número.
        b (float): El segundo número.

    Returns:
        float: El resultado de la suma.
    """
    return a + b

def producto(a, b):
    """
    Calcula el producto de dos números.

    Args:
        a (float): El primer número.
        b (float): El segundo número.

    Returns:
        float: El resultado del producto.
    """
    return a * b
